"""
Funciones auxiliares círculo y circunferencia
"""

from math import pi

def area(radio):
    """
    area(radio) --> devuelve área
    """
    return pi * radio * radio

def circunferencia(radio):
    """
    circunferencia(radio) --> devuelve longitud circunf.
    """
    return pi * 2 * radio
